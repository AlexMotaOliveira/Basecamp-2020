#include <stdlib.h>
#include <stdio.h>

int		*ft_range(int min, int max)
{
	int		*array;
	int		i;

	if (min >= max)
		return (0);
	if (!(array = (int *)malloc(sizeof(int) * (max - min))))
		return (0);
	i = 0;
	while (min < max)
	{
		array[i] = min;
		i++;
		min++;
	}
	return (array);
}
